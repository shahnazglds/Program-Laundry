    private void tCariOutletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tCariOutletActionPerformed
        // TODO add your handling code here:
        String cari = tCariOutlet.getText();
        DefaultTableModel tableModel = (DefaultTableModel) tblOutlet.getModel();
        tableModel.setRowCount(0);

        try {
            String query = "SELECT * FROM tb_outlet WHERE id_outlet LIKE ? "
                    + "OR nama LIKE ? OR alamat LIKE ?";
            this.stat = k.getCon().prepareStatement(query);
            this.stat.setString(1, "%" + cari + "%");
            this.stat.setString(2, "%" + cari + "%");
            this.stat.setString(3, "%" + cari + "%");
            this.rs = stat.executeQuery();
            int rowNum = 1;

            while (rs.next()) {
                Object[] data = {
                    rowNum,          // Add the row number
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                tableModel.addRow(data);
                rowNum++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, 
                    e.getMessage());
        }
    }//GEN-LAST:event_tCariOutletActionPerformed

    private void cbxUrutkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxUrutkanActionPerformed
        // TODO add your handling code here:
        String cari = tCariOutlet.getText();
        String urutan = cbxUrutkan.getSelectedItem().toString();

        DefaultTableModel tableModel = (DefaultTableModel) tblOutlet.getModel();
        tableModel.setRowCount(0);

        try {
            String query;
            if (urutan.equals("PILIH")) {
                query = "SELECT * FROM tb_outlet WHERE id_outlet LIKE ? OR nama"
                        + " LIKE ? OR alamat LIKE ?";
            } else {
                query = "SELECT * FROM tb_outlet WHERE id_outlet LIKE ? OR nama"
                        + " LIKE ? OR alamat LIKE ? ORDER BY ";
                if (urutan.equals("ID Outlet")) {
                    query += "id_outlet";
                } else if (urutan.equals("Nama Outlet")) {
                    query += "nama";
                } else if (urutan.equals("Alamat")) {
                    query += "alamat";
                }
            }

            this.stat = k.getCon().prepareStatement(query);
            this.stat.setString(1, "%" + cari + "%");
            this.stat.setString(2, "%" + cari + "%");
            this.stat.setString(3, "%" + cari + "%");

            this.rs = stat.executeQuery();
            int rowNum = 1;

            while (rs.next()) {
                Object[] data = {
                    rowNum,
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                tableModel.addRow(data);
                rowNum++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, 
                    e.getMessage());
        }
    }//GEN-LAST:event_cbxUrutkanActionPerformed

