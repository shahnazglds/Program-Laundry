    public void refreshTable () {
        model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("No Telepon");
        tblOutlet.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * "
                    + "from tb_outlet");
            this.rs = stat.executeQuery();
            int rowNum = 1;
            
            while (rs.next()) {
                Object[] data = {
                    rowNum,
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                model.addRow(data);
                rowNum++;
            }
            TableColumnModel columnModel = tblOutlet.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(20);
            columnModel.getColumn(1).setPreferredWidth(30);         
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        tIdOutlet.setText("");
        tNamaOutlet.setText("");
        tAlamatOutlet.setText("");
        tNoTelepon.setText("");
        Renderer.setCenterBoldColumnHeaders(tblOutlet);
    }
    
